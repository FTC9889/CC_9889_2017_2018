def divideMaintainNumSign(num, dem):
    result = num/dem

    if ((num >= 0) & (result < 0)) or ((num < 0) & (result > 0)):
        result *= -1

    return  result


class Arc: # Used for curves and turns
    def calculate(distance, radius):
        # Without this, 0 radius turn will divide by 0 and that's bad.
        if radius != 0:
            # Arc length in center of robot
            angleInRadians = distance/radius

            # Convert to degrees
            theda = math.degrees(angleInRadians)

            # Get the left and right arc lengths.
            left_dis = angleInRadians*(radius+wheelbase/2)
            right_dis = angleInRadians*(radius-wheelbase/2)

            # x and y displacements
            x = wheelbase/2 * math.sin(theda)
            y = wheelbase/2 * math.cos(theda)
        else:
            # If radius = 0, there is no change in x and y.
            x = 0
            y = 0

            if distance == 0.0:
                left_dis = 0
                right_dis = 0
                theda = 0.0
            elif distance < 0.0:
                left_dis = distance
                right_dis = -distance
                arc_length = distance+wheelbase/2
                angleInRadians = arc_length/(wheelbase/2)
                theda = math.degrees(angleInRadians)
            elif distance > 0.0:
                left_dis = distance
                right_dis = -distance
                arc_length = distance-wheelbase/2
                angleInRadians = arc_length/(wheelbase/2)
                theda = math.degrees(angleInRadians)

        return [x, y, theda, left_dis, right_dis]

    ## Used to Calculate distances with a angle, wheelbase, and radius of the turn
    ## It the radius = 0, zero point turn
    def getDistanceFromDegrees(wantedAngle, wheelbase=16, radiusOfTurn=0):
        running = True
        timesRun = 0
        offset = 0.1
        distance = 0.0
        if radiusOfTurn == 0:
            for i in range(7):
                while(running):
                    timesRun +=1
                    if distance == 0.0:
                        left_dis = 0
                        right_dis = 0
                        theda = 0.0
                    elif distance < 0.0:
                        left_dis = distance
                        right_dis = -distance
                        arc_length = distance+wheelbase/2
                        angleInRadians = arc_length/(radiusOfTurn+(wheelbase/2))
                        theda = math.degrees(angleInRadians)
                    elif distance > 0.0:
                        left_dis = -distance
                        right_dis = distance
                        arc_length = distance-wheelbase/2
                        angleInRadians = arc_length/(radiusOfTurn+(wheelbase/2))
                        theda = math.degrees(angleInRadians)

                    if wantedAngle < 0:
                        if theda <= wantedAngle:
                            running = False
                        else:
                            distance -= offset
                    else:
                        if theda >= wantedAngle:
                            running = False
                        else:
                            distance += offset

                    if timesRun > 10000:
                        running = False

                if wantedAngle < 0:
                    distance = distance+(offset*2)
                else:
                    distance = distance-(offset*2)
                offset = offset/100
                running = True
        else:
            distance = 0.0
            timesRun = 0
            while True:
                if timesRun < (abs(wantedAngle)*100000):
                    theda = Arc.calculate(distance, radiusOfTurn)[2]
                    if wantedAngle > 0:
                        if  theda >= wantedAngle:
                            break
                        else:
                            distance += 0.00001
                            timesRun += 1
                    elif wantedAngle < 0:
                        if theda <= wantedAngle:
                            break
                        else:
                            distance -= 0.00001
                            timesRun += 1
                else:
                    break

        return [theda, distance, radiusOfTurn, timesRun]


